export type HttpMethod = 'GET' | 'POST' | 'PUT' | 'PATCH' | 'DELETE';

export type ApiRouteMeta = {
  method: HttpMethod;
  path: string; // e.g. "/api/posts/:id"
};


export function readApiRoutes(
  projectRoot: string,
  apiFile: string,
  apiBasePath: string,
): ApiRouteMeta[] {
  const fullPath = path.resolve(projectRoot, apiFile);

  if (!fs.existsSync(fullPath)) {
    return [];
  }

  const code = fs.readFileSync(fullPath, 'utf-8');

  const routes: ApiRouteMeta[] = [];

  // qualquer IDENT.get('/path'), api.get('/demo'), router.post('/login'), etc
  const regex =
    /\b([A-Za-z_$][\w$]*)\s*\.(get|post|put|patch|delete)\(\s*['"`]([^'"`]+)['"`]/gi;

  let match: RegExpExecArray | null;

  // normaliza base: '/api/' ou '/' etc
  const base =
    apiBasePath === '/'
      ? ''
      : apiBasePath.endsWith('/')
        ? apiBasePath.slice(0, -1)
        : apiBasePath;

  while ((match = regex.exec(code)) != null) {
    const method = match[2].toUpperCase() as HttpMethod;
    const routePath = match[3]; // '/demo', '/posts/:id', etc.

    // garante que o path comece com '/'
    const cleanRoute =
      routePath.startsWith('/') ? routePath : '/' + routePath;

    // aplica o prefixo da API: '/api' + '/demo' => '/api/demo'
    const fullRoute =
      base + cleanRoute; // se base = '' => fica só '/demo'

    routes.push({ method, path: fullRoute });
  }

  return routes;
}

/**
 * Very simple name generator for a route.
 * Example:
 *   method: GET, path: /api/posts/:id/comments
 *   => "get_api_posts_by_id_comments"
 */
export function routeToName({ method, path }: ApiRouteMeta): string {
  const parts = path
    .replace(/^\/+/, '') // remove leading slash
    .split('/')
    .map((part) => {
      if (part.startsWith(':')) {
        return 'by_' + part.slice(1);
      }
      if (!part) return '';
      return part.replace(/[^a-zA-Z0-9]+/g, '_');
    })
    .filter(Boolean);

  return [method.toLowerCase(), ...parts].join('_');
}


