/**
 * Function that generates a string to be used to generate a file with it's content.
 * @param routesDir Routes directory
 * @returns A string to generate the file
 */
function createQueryFileContent(projectRoot: string, opts: ResolvedBunaConfig): string {
  const apiRoutes = readApiRoutes(projectRoot, opts.apiFile, opts.apiBasePath);

  if (apiRoutes.length === 0) {
    return `// AUTO-GENERATED BY buna-plugin. NO API ROUTES FOUND.
export const api = {} as const;
export type ApiClient = typeof api;
`;
  }


  const header = `// AUTO-GENERATED BY buna-plugin. DO NOT EDIT.
import { createFetcherStore, createMutatorStore } from '@buna/router/query';
`;

  // Group routes by first segment after /api
  const groups: Record<string, ApiRouteMeta[]> = {};

  for (const route of apiRoutes) {
    const parts = route.path.replace(/^\/+/, '').split('/'); // ['api', 'posts', ':id']
    const resource = parts[1] || 'root';

    if (!groups[resource]) groups[resource] = [];
    groups[resource].push(route);
  }

  const bodyLines: string[] = [];
  bodyLines.push('export const api = {');

  for (const [resource, routes] of Object.entries(groups)) {
    bodyLines.push(`  ${resource}: {`);

    for (const route of routes) {
      const parts = route.path.replace(/^\/+/, '').split('/');
      const restParts = parts.slice(2); // depois de 'api' e 'resource'

      const hasParams = route.path.includes(':');
      const paramRegex = /:([a-zA-Z0-9_]+)/g;
      const paramNames: string[] = [];
      let m: RegExpExecArray | null;
      while ((m = paramRegex.exec(route.path)) != null) {
        paramNames.push(m[1]);
      }

      const pathTemplate =
        '`' +
        route.path.replace(/:([a-zA-Z0-9_]+)/g, '${$1}') +
        '`';

      // Decide operation name
      const opName = getOperationName(route.method, restParts, paramNames);

      if (route.method === 'GET') {
        if (!hasParams) {
          // GET /api/resource -> list
          bodyLines.push(
            `    // ${route.method} ${route.path}`,
            `    ${opName}: createFetcherStore<any>(['${route.method}', '${route.path}']),`,
          );
        } else {
          // GET with params -> function returning store
          const paramsSig = paramNames.map((p) => `${p}: string`).join(', ') || 'id: string';

          bodyLines.push(
            `    // ${route.method} ${route.path}`,
            `    ${opName}(${paramsSig}) {`,
            `      const path = ${pathTemplate};`,
            `      return createFetcherStore<any>(['${route.method}', path]);`,
            `    },`,
          );
        }
      } else {
        // Mutations: POST, PUT, PATCH, DELETE
        if (!hasParams) {
          // no params -> store direto
          bodyLines.push(
            `    // ${route.method} ${route.path}`,
            `    ${opName}: createMutatorStore<any>(async ({ data, revalidate }) => {`,
            `      await fetch('${route.path}', {`,
            `        method: '${route.method}',`,
            `        headers: { 'Content-Type': 'application/json' },`,
            `        body: data ? JSON.stringify(data) : undefined,`,
            `      });`,
            `      revalidate((key) => typeof key === 'string' && key.includes('${route.path}'));`,
            `    }),`,
          );
        } else {
          // com params -> função que cria mutator store
          const paramsSig = paramNames.map((p) => `${p}: string`).join(', ') || 'id: string';

          bodyLines.push(
            `    // ${route.method} ${route.path}`,
            `    ${opName}(${paramsSig}) {`,
            `      const path = ${pathTemplate};`,
            `      return createMutatorStore<any>(async ({ data, revalidate }) => {`,
            `        await fetch(path, {`,
            `          method: '${route.method}',`,
            `          headers: { 'Content-Type': 'application/json' },`,
            `          body: data ? JSON.stringify(data) : undefined,`,
            `        });`,
            `        revalidate((key) => typeof key === 'string' && key.includes(path));`,
            `      });`,
            `    },`,
          );
        }
      }
    }

    bodyLines.push('  },');
  }

  bodyLines.push('} as const;');
  bodyLines.push('');
  bodyLines.push('export type ApiClient = typeof api;');

  return `${header}

${bodyLines.join('\n')}
`;
}

// Helper to decide operation name
function getOperationName(
  method: string,
  restParts: string[],
  paramNames: string[],
): string {
  const hasParams = paramNames.length > 0;
  const methodUpper = method.toUpperCase();

  if (methodUpper === 'GET') {
    if (!hasParams && restParts.length === 0) return 'list';
    if (hasParams && restParts.length === 1) {
      const name = paramNames[0] || 'id';
      if (name === 'id') return 'byId';
      return 'by' + name[0].toUpperCase() + name.slice(1);
    }
    // GET /api/resource/something
    const suffix = restParts.join('_').replace(/[^a-zA-Z0-9_]+/g, '');
    return 'get' + (suffix ? '_' + suffix : '');
  }

  if (methodUpper === 'POST') {
    if (!hasParams && restParts.length === 0) return 'create';
    const suffix = restParts.join('_').replace(/[^a-zA-Z0-9_]+/g, '');
    return 'create' + (suffix ? '_' + suffix : '');
  }

  if (methodUpper === 'PUT') {
    if (hasParams) return 'update';
    return 'updateAll';
  }

  if (methodUpper === 'PATCH') {
    if (hasParams) return 'patch';
    return 'patchAll';
  }

  if (methodUpper === 'DELETE') {
    if (hasParams) return 'remove';
    return 'removeAll';
  }

  // fallback
  const suffix = restParts.join('_').replace(/[^a-zA-Z0-9_]+/g, '');
  return method.toLowerCase() + (suffix ? '_' + suffix : '');
}

function generateAll(projectRoot: string, opts: ResolvedBunaConfig) {
  const clientContent = createClientFileContent(opts.routesDir)
  writeFileSafe(projectRoot, opts.outputClient, clientContent)

  const honoContent = createHonoFileContent(opts.routesDir)
  writeFileSafe(projectRoot, opts.outputHono, honoContent)

  const queryContent = createQueryFileContent(projectRoot, opts);
  writeFileSafe(projectRoot, opts.outputQuery, queryContent);

  const typesContent = createRoutesTypesFileContent(projectRoot, opts.routesDir);
  writeFileSafe(projectRoot, opts.outputTypes, typesContent);
}

export function buna(options: BunaConfig = {}): Plugin {
  let root = process.cwd()
  let viteConfig: ResolvedConfig;
  let resolved: ResolvedBunaConfig;

  return {
    name: 'buna',
    enforce: 'pre',

    configResolved(config) {
      viteConfig = config;
      resolved = resolveOptions(options);

      // Geração inicial
      generateAll(config.root, resolved);
    },

    buildStart() {
      if (viteConfig) {
        generateAll(viteConfig.root, resolved);
      }
    },

    configureServer(server) {
      const projectRoot = viteConfig.root;

      // Devtools scaffold endpoint
      server.middlewares.use(
        '/__buna/devtools/scaffold-route',
        (req, res, next) => {
          if (req.method !== 'POST') {
            return next();
          }

          const chunks: Buffer[] = [];

          req.on('data', (chunk) => {
            chunks.push(chunk);
          });

          req.on('end', () => {
            try {
              const raw = Buffer.concat(chunks).toString('utf-8');
              const payload = raw ? JSON.parse(raw) : {};

              // Example: "src/routes/new-route.tsx"
              const relativePath: string =
                payload.path || path.join(resolved.routesDir, 'new-route.tsx');

              const componentName: string = payload.name || 'NewRoutePage';

              const absPath = path.resolve(projectRoot, relativePath);
              const routesDirAbs = path.resolve(projectRoot, resolved.routesDir);

              // Security: ensure route is inside routesDir
              if (!absPath.startsWith(routesDirAbs)) {
                res.statusCode = 400;
                res.setHeader('Content-Type', 'application/json');
                res.end(
                  JSON.stringify({
                    ok: false,
                    error: 'Route path must be inside routesDir',
                  }),
                );
                return;
              }

              // Derive routePath from relativePath (e.g. "src/routes/posts.tsx" -> "/posts")
              const routeRelativeToRoutesDir = path
                .relative(resolved.routesDir, relativePath)
                .replace(/\\/g, '/'); // normalize Windows paths

              let routeSubPath = routeRelativeToRoutesDir.replace(/\.tsx?$/, '');
              let routePath = '/' + routeSubPath;

              // Normalize index files to "/" or "/foo"
              if (routePath === '/index' || routePath === '//') {
                routePath = '/';
              }
              routePath = routePath.replace(/\/index$/, '');
              routePath = routePath.replace(/\/+/g, '/');

              if (!routePath) {
                routePath = '/';
              }

              // Ensure directory exists
              const dir = path.dirname(absPath);
              if (!fs.existsSync(dir)) {
                fs.mkdirSync(dir, { recursive: true });
              }

              // Only write if file does not exist
              if (!fs.existsSync(absPath)) {
                const content = scaffoldContent({
                  componentName,
                  relativePath,
                  routePath,
                });

                fs.writeFileSync(absPath, content, 'utf-8');
              }

              // Regenerate .buna files so the new route is registered
              generateAll(projectRoot, resolved);

              res.statusCode = 200;
              res.setHeader('Content-Type', 'application/json');
              res.end(
                JSON.stringify({
                  ok: true,
                  path: relativePath,
                }),
              );
            } catch (err: any) {
              console.error('[buna devtools scaffold] error:', err);
              res.statusCode = 500;
              res.setHeader('Content-Type', 'application/json');
              res.end(
                JSON.stringify({
                  ok: false,
                  error: String(err?.message ?? err),
                }),
              );
            }
          });
        },
      );

      // Devtools "open in editor" endpoint
      server.middlewares.use(
        '/__buna/devtools/open-route',
        (req, res, next) => {
          if (req.method !== 'POST') {
            return next();
          }

          const chunks: Buffer[] = [];

          req.on('data', (chunk) => {
            chunks.push(chunk);
          });

          req.on('end', () => {
            try {
              const raw = Buffer.concat(chunks).toString('utf-8');
              const payload = raw ? JSON.parse(raw) : {};

              // Aqui "path" é o path de ROTA, ex: "/settings", "/posts/:id", "/"
              const routePath: string | undefined = payload.path;
              if (!routePath || typeof routePath !== 'string') {
                res.statusCode = 400;
                res.setHeader('Content-Type', 'application/json');
                res.end(
                  JSON.stringify({
                    ok: false,
                    error: 'Missing "path" (routePath) in payload',
                  }),
                );
                return;
              }

              // Normalize route to files`s subpath
              // "/", ""          -> "index"
              // "/settings"      -> "settings"
              // "/posts/:id"     -> "posts/[id]"
              // "/blog/"         -> "blog"
              let routeSubPath = routePath.replace(/^\/+/, ''); // tira "/" inicial

              if (!routeSubPath) {
                routeSubPath = 'index';
              }

              if (routeSubPath.endsWith('/')) {
                routeSubPath = routeSubPath.slice(0, -1) || 'index';
              }

              // segmentos dinâmicos :id -> [id]
              routeSubPath = routeSubPath.replace(
                /:([A-Za-z0-9_]+)/g,
                '[$1]',
              );

              // monta caminho relativo dentro de routesDir
              const relativePath = path.join(
                resolved.routesDir,
                `${routeSubPath}.tsx`,
              );

              const absPath = path.resolve(projectRoot, relativePath);
              const routesDirAbs = path.resolve(projectRoot, resolved.routesDir);

              console.log('[open-route] routePath:', routePath);
              console.log('[open-route] routeSubPath:', routeSubPath);
              console.log('[open-route] relativePath:', relativePath);
              console.log('[open-route] absPath:', absPath);
              console.log('[open-route] routesDirAbs:', routesDirAbs);

              // Security: apenas arquivos dentro de routesDir
              if (!absPath.startsWith(routesDirAbs)) {
                res.statusCode = 400;
                res.setHeader('Content-Type', 'application/json');
                res.end(
                  JSON.stringify({
                    ok: false,
                    error: 'Route path must be inside routesDir',
                  }),
                );
                return;
              }

              const target = `/__open-in-editor?file=${encodeURIComponent(absPath)}`;

              res.statusCode = 302;
              res.setHeader('Location', target);
              res.end();
            } catch (err: any) {
              console.error('[buna devtools open-route] error:', err);
              res.statusCode = 500;
              res.setHeader('Content-Type', 'application/json');
              res.end(
                JSON.stringify({
                  ok: false,
                  error: String(err?.message ?? err),
                }),
              );
            }
          });
        },
      );

      const routesDirAbs = path.resolve(projectRoot, resolved.routesDir);
      const serverDirAbs = path.resolve(projectRoot, resolved.serverDir);

      // Ensure watcher tracks these directories
      server.watcher.add([routesDirAbs, serverDirAbs]);

      const onFileChange = (file: string) => {
        const abs = path.resolve(file);

        const inRoutes = abs.startsWith(routesDirAbs);
        const inServer = abs.startsWith(serverDirAbs);

        if (!inRoutes && !inServer) return;

        // Optional: ignore non-code files
        const ext = path.extname(abs);
        if (!['.ts', '.tsx', '.js', '.jsx'].includes(ext)) return;

        // Regenerate .buna files whenever something in routes/ or server/ changes
        generateAll(projectRoot, resolved);
      };

      server.watcher.on('add', onFileChange);
      server.watcher.on('change', onFileChange);
      server.watcher.on('unlink', onFileChange);
    }
  }
}



